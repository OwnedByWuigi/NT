//----------------------------------------------------------------------------------------
// 
// Compares rgba to f, and if the difference is within a small tolerance, return true.
// Special handling for DXGI_FORMAT_R10G10B10A2_UNORM that has so little bit for alpha 
// that it needs more tolerance.
//
bool CUAVBufferTest::EqualWithinTolerance( float r, float g, float b, float a, FLOAT_COLOR f, DXGI_FORMAT format )
{
	float diffR = f.r - r;
	float diffG = f.g - g;
	float diffB = f.b - b;
	float diffA = f.a - a;

	if ( diffR < -UAV_BUFFER_TYPED_TEST_FLOAT_TOLERANCE || UAV_BUFFER_TYPED_TEST_FLOAT_TOLERANCE < diffR )
	{
		return false;
	}
	if ( diffG < -UAV_BUFFER_TYPED_TEST_FLOAT_TOLERANCE || UAV_BUFFER_TYPED_TEST_FLOAT_TOLERANCE < diffG )
	{
		return false;
	}
	if ( diffB < -UAV_BUFFER_TYPED_TEST_FLOAT_TOLERANCE || UAV_BUFFER_TYPED_TEST_FLOAT_TOLERANCE < diffB )
	{
		return false;
	}
	if ( format == DXGI_FORMAT_R10G10B10A2_UNORM )
	{
		// needs lots of tolerance - only has 2 bits....
		if ( diffA < -0.3 || 0.3 < diffA )
		{
			return false;
		}
	}
	else
	{
		if ( diffA < -UAV_BUFFER_TYPED_TEST_FLOAT_TOLERANCE || UAV_BUFFER_TYPED_TEST_FLOAT_TOLERANCE < diffA )
		{
			return false;
		}
	}

	return true;
}

